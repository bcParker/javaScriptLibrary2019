//Assignment Operator
let x = 20;

//Addition Assignment Operator
x += 1; //x = x + 1
console.log(x);

//Subtraction Assignment Operator
x -= 1; //x = x - 1
console.log(x);

//Multiplication Assignment Operator
x *= 2; //x = x * 2
console.log(x);

//Division Assignment Operator
x /= 4; //x = x / 2
console.log(x);

//Remainder (modulus) Assignment Operator
x %= 2; //x = x % 2
console.log(x);

//Modulus Operator '%'
console.log(5%3); // 5/3 -> 1 2/3 -> 2 (leftover after dividing)
console.log(25%7); // 25/7 -> 3 4/7 -> 4

//Exponential Assignment Operator
x **= 2; // x to the power of 2
console.log(x);

let y = 7;
y **= 3; //y to the power of 3
console.log(y);